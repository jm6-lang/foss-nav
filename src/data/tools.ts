// 获取某分类下的工具
export function getToolsByCategory(categoryId: string): Tool[] {
  return tools.filter(t => t.category === categoryId);
}

// 搜索工具
export function searchTools(query: string): Tool[] {
  const q = query.toLowerCase();
  return tools.filter(t =>
    t.name.toLowerCase().includes(q) ||
    t.nameEn.toLowerCase().includes(q) ||
    t.description.toLowerCase().includes(q) ||
    t.tags.some(tag => tag.toLowerCase().includes(q))
  );
}

// 获取某个工具的替代品
export function getAlternatives(toolId: string): Tool[] {
  const tool = tools.find(t => t.id === toolId);
  if (!tool?.alternatives) return [];
  return tools.filter(t => tool.alternatives!.includes(t.id));
}

// 获取相关推荐（同分类 + 相似标签）
export function getRelated(toolId: string, limit = 4): Tool[] {
  const tool = tools.find(t => t.id === toolId);
  if (!tool) return [];
  return tools
    .filter(t => t.id !== toolId && (
      t.category === tool.category ||
      t.tags.some(tag => tool.tags.includes(tag))
    ))
    .slice(0, limit);
}
